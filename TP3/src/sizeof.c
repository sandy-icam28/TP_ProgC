#include <stdio.h>

int main () {
    printf("La taille de int est : %d\n", sizeof(int));
    printf("La taille de int* est : %d\n", sizeof(int*));
    printf("La taille de int** est : %d\n", sizeof(int**));
    printf("La taille de char* est : %d\n", sizeof(char*));
    printf("La taille de char** est : %d\n", sizeof(char**));
    printf("La taille de char*** est : %d\n", sizeof(char***));
    printf("La taille de float* est : %d\n", sizeof(float*));
    printf("La taille de float** est : %d\n", sizeof(float**));
    printf("La taille de float*** est : %d\n", sizeof(float***));

}
